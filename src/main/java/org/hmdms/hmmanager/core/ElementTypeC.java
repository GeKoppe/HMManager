package org.hmdms.hmmanager.core;

/**
 * Indicator, which class is extending the current {@link Element} object
 */
public enum ElementTypeC {
    /**
     * Indicator, that an {@link Element} object is a {@link Document} object.
     */
    DOCUMENT,
    /**
     * Indicator, that an {@link Element} object is a {@link Structure} object.
     */
    STRUCTURE
}
